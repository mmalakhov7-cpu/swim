/** Готовые тренировки как WorkoutTemplate (со стабильными id заданий). */
export function presetTemplates() {
  return PRESET_WORKOUTS.map((w) => ({
    id: w.id,
    name: w.name,
    tasks: w.tasks.map((t, i) => ({
      id: `${w.id}_t${i + 1}`,
      name: t.n,
      targetDistance: t.d,
      stroke: "кроль",
      restAfterSec: t.r ?? null,
      note: t.note ?? null,
    })),
  }));
}

