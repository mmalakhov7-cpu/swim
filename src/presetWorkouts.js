// длинных монотонных кусков: объём набирается короткими блоками (≤400) с
// внутренним разнообразием — по 50 / по 100 / лесенка дыхания / негатив-сплит /
// build — либо интервалами. Инвентарь — только средние и большие лопатки.
// Каждый подсет = отдельное задание (экран показывает N/M и ведёт свайпом).
//
// Обозначения:
//   6-удар / 4-удар / 2-удар — удары ногами кролем на цикл рук
//   дых. N — вдох каждые N гребков (больше = сильнее гипоксия)
//   N/M по 25 — первые 25 на N гребков, вторые 25 на M
//   негатив — 2-й полтинник сотни быстрее; build — каждая часть быстрее
//   ускорение / средний / спокойно — интенсивность

